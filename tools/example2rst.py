if __name__ == "__main__":
    try:
        output = get_output(input_name)
    except:
        apsw.ext.print_augmented_traceback(*sys.exc_info())
        raise
    with tempfile.NamedTemporaryFile("wt", prefix="example2rst") as f:
        gen_rst(input_name, f, output)
